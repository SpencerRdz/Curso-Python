x = 10
y = 3

print("Suma:", x + y)          # Suma: 13
print("Resta:", x - y)         # Resta: 7
print("Multiplicación:", x * y) # Multiplicación: 30
print("División:", x / y)       # División: 3.3333333333333335
print("División entera:", x // y) # División entera: 3
print("Módulo:", x % y)         # Módulo: 1
print("Exponenciación:", x ** y) # Exponenciación: 1000
print("Raíz cuadrada de x:", x ** 0.5) # Raíz cuadrada de x: 3.1622776601683795
print("Raíz cúbica de x:", x ** (1/3)) # Raíz cúbica de x: 2.154434690031884
